/// Classe que contém os caminhos dos assets da aplicação.
/// Para acessar os assets, basta utilizar o caminho:
///
/// ```
/// AppAssets.designSystemPackage + '/assets/images/logo.png'
/// ```
///
/// Onde `AppAssets.designSystemPackage` é o caminho para o pacote
/// `design_system` e `/assets/images/logo.png` é o caminho para o
/// asset desejado.
///
/// Para acessar os assets de outros pacotes, basta criar uma nova
/// constante com o nome do pacote e o caminho para o asset desejado.
///
/// ```
/// static const String designSystemPackage = 'design_system';
/// ```
///
class AppAssets {
  static const String designSystemPackage = 'design_system';
}
